public class HitungJumlahRataRata {
    public static void main(String[] args) {
        double nilai1 = 91;
        double nilai2 = 90;
        double nilai3 = 93;
        double nilai4 = 92;
        double nilai5 = 90;
    
        double jumlah = nilai1 + nilai2 + nilai3 + nilai4 + nilai5;
        double rataRata = jumlah / 5;
        System.out.println("Jumlah dari lima nilai tersebut adalah: " + jumlah);
        System.out.println("Rata-rata dari lima nilai tersebut adalah: " + rataRata);
    }
}
